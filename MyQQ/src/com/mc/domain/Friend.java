package com.mc.domain;
/**
 * QQ好友信息结构
 * @author Shine_MuShi
 *
 */
public class Friend {
	/**
	 * 好友QQ号
	 */
	private String qq;
	/**
	 * 好友昵称
	 */
	private String nick;
	
	public String getQq() {
		return qq;
	}
	public void setQq(String qq) {
		this.qq = qq;
	}
	public String getNick() {
		return nick;
	}
	public void setNick(String nick) {
		this.nick = nick;
	}
	
}
